#######################################

### Basics

# 1. Always use a text editor.
# 2. Indent your code by at least 4 spaces.
# 3. Limit the width of your code. 80 columns is a recommended value.
# 4. Limit the length of individual functions. This not only helps readability
#    but also when debugging (the debuggers says in which function the error
#    occurs).