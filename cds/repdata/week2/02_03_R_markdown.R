### What Is R Markdown?

# R Markdown integrates markdown with R code. The R code is evaluated when the 
# document is processed, so the R code is sure to be valid. This "live" R code
# is usually inserted in code chunks into the text.
#
# HTML and LaTeX can also be added directly to R markdown.
#
# R markdown is a core tool of literate statistical programming.


### R Markdown Workflow

# R markdown needs to first be converted to normal markdown, which can be done
# with the knitr package. Afterwards the markdown can be converted to HTML. 
# Both can easily be managed in RStudio.
# 
# In principle any texteditor would work as well though.